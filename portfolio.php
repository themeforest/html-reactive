    /* portfolio */
    $('.btn-filter').click(function() {
        $('.btn-filter').removeClass('selected');
        $(this).addClass('selected');
        
        var tag = $(this).data('tag');
        var box = $('.tag-box');
        box.stop(true, true)
        .animate({
        opacity: '0'},{
        duration: 300,
        complete: function(){
            box.hide();
            $('.tag-'+tag).show();
            $('.tag-'+tag).animate({opacity: '1'}, {
                duration: 300
            });
            
            if(tag == '')
            {
                box.show();
                box.animate({opacity: '1'}, {
                    duration: 300
                });
            }
        }});
    });
